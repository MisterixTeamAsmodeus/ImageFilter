#pragma once

/**
 * @brief Коды ошибок для обработки изображений
 * 
 * Коды ошибок сгруппированы по категориям:
 * - Success (0): Успешное выполнение
 * - Validation (1-99): Ошибки валидации параметров и данных
 * - Memory (100-199): Ошибки выделения памяти
 * - Format (200-299): Ошибки формата изображения
 * - Computation (300-399): Ошибки вычислений (переполнение, деление на ноль и т.д.)
 * - FileOperation (400-499): Ошибки файловых операций
 * - System (500-599): Системные ошибки (errno)
 */
enum class FilterError
{
    // Успешное выполнение
    Success = 0,

    // Ошибки валидации (1-99)
    InvalidImage = 1,              ///< Изображение не загружено или невалидно
    InvalidChannels = 2,            ///< Некорректное количество каналов
    InvalidParameter = 3,          ///< Некорректный параметр фильтра
    InvalidWidth = 4,              ///< Некорректная ширина изображения
    InvalidHeight = 5,             ///< Некорректная высота изображения
    InvalidSize = 6,               ///< Некорректный размер изображения
    InvalidRadius = 7,             ///< Некорректный радиус (должен быть > 0)
    InvalidFactor = 8,             ///< Некорректный коэффициент (должен быть > 0)
    InvalidThreshold = 9,          ///< Некорректное пороговое значение
    InvalidQuality = 10,           ///< Некорректное качество (должно быть в диапазоне 0-100)
    InvalidAngle = 11,             ///< Некорректный угол поворота
    InvalidKernelSize = 12,        ///< Некорректный размер ядра свертки
    ParameterOutOfRange = 13,      ///< Параметр вне допустимого диапазона
    EmptyImage = 14,               ///< Изображение пустое (нулевой размер)

    // Ошибки памяти (100-199)
    OutOfMemory = 100,             ///< Недостаточно памяти для выделения буфера
    MemoryAllocationFailed = 101,  ///< Ошибка выделения памяти
    MemoryOverflow = 103,          ///< Переполнение памяти при вычислениях

    // Ошибки формата (200-299)
    UnsupportedFormat = 200,       ///< Неподдерживаемый формат изображения
    UnsupportedChannels = 201,     ///< Неподдерживаемое количество каналов
    CorruptedImage = 202,          ///< Поврежденное изображение
    InvalidImageData = 203,         ///< Некорректные данные изображения
    FormatMismatch = 204,          ///< Несоответствие формата ожидаемому

    // Ошибки вычислений (300-399)
    DivisionByZero = 300,          ///< Деление на ноль
    ArithmeticOverflow = 301,      ///< Арифметическое переполнение
    ArithmeticUnderflow = 302,     ///< Арифметическое исчерпание
    InvalidKernel = 303,           ///< Некорректное ядро свертки
    KernelNormalizationFailed = 304, ///< Ошибка нормализации ядра

    // Ошибки файловых операций (400-499)
    FileOperationFailed = 400,     ///< Общая ошибка файловой операции
    FileNotFound = 401,            ///< Файл не найден
    FileReadError = 402,           ///< Ошибка чтения файла
    FileWriteError = 403,          ///< Ошибка записи файла
    FilePermissionDenied = 404,    ///< Отказано в доступе к файлу
    FileTooLarge = 405,            ///< Файл слишком большой
    InvalidFilePath = 406,         ///< Некорректный путь к файлу

    // Системные ошибки (500-599)
    SystemError = 500,             ///< Общая системная ошибка
    InvalidSystemCall = 501        ///< Некорректный системный вызов
};
